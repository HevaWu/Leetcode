/*
Solution 1:
use array to store inorder traversal result of tree

Time Complexity: O(1)
Space Complexity: O(n)
*/
/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     public var val: Int
 *     public var left: TreeNode?
 *     public var right: TreeNode?
 *     public init(_ val: Int) {
 *         self.val = val
 *         self.left = nil
 *         self.right = nil
 *     }
 * }
 */

class BSTIterator {

    var arr: [Int] = [Int]()
    var cur = 0
    
    init(_ root: TreeNode?) {
        inorder(root)
    }
    
    func inorder(_ root: TreeNode?) {
        guard let node = root else { return }
        inorder(node.left)
        arr.append(node.val)
        inorder(node.right)
    }
    
    /** @return the next smallest number */
    func next() -> Int {
        cur += 1
        return arr[cur-1]
    }
    
    /** @return whether we have a next smallest number */
    func hasNext() -> Bool {
        return cur < arr.count
    }
}

